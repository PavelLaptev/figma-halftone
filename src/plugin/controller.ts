// Show UI
figma.showUI(__html__, { width: 360, height: 600 });

////////////////////////////////////////////////////////////////
///////////////////////// ON MESSAGE ///////////////////////////
////////////////////////////////////////////////////////////////

figma.ui.onmessage = async msg => {
  let allPages = figma.root.children;

  let stats = allPages;

  // UPDATE ON BY ONE
  if (msg.type === "request-stats") {
    figma.ui.postMessage({
      type: "stats",
      data: stats
    });
  }
};
